# 파이썬의 기본적인 변수 선언과 변수값 할당을 보여주는 코드를 구현하시오.

# 변수 선언 및 변수 값 할당
# 파이썬은 변수를 선언시 메모리 공간을 예약하기 위해서 명시적으로 선언을 할 필요가 없다.
# 따라서, 바로 변수명을 적어주고 값을 할당하면 자동으로 변수 선언 및 변수 값 할당이 이루어진다.
a = 100
b = 3.14
c = 'k'
d = 'Hello World'

print('a 변수의 값은? ', a)
print('b 변수의 값은? ', b)
print('c 변수의 값은? ', c)
print('d 변수의 값은? ', d)
print(a, b, c, d)  # 100 3.14 k Hello World
